public class App {
    public static void main(String[] args) throws Exception {
        int dato1 = 30;
        int dato2 = 20;

        int total = 0;
        int total2 = 0;
        int total3 = 0;

        total = dato1 + dato2;
        total2 = dato1 - dato2;
        total3 = dato1 * dato2;

        System.out.println("Total de la suma: " + total);
        System.out.println("Total de la resta: " + total2);
        System.out.println("Total de la multiplacion: " + total3);
    }
}